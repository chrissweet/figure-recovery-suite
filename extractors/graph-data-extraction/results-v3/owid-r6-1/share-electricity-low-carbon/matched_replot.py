"""Matched replot for owid-r6-1 / share-electricity-low-carbon.

Renders the extracted data.csv at the same pixel dimensions and approximate
visual style as the source image (850 x 600). Used as Phase-4 close-the-loop
verification per SKILL.md.
"""
import csv
import os
from collections import defaultdict

import matplotlib.image as mpimg
import matplotlib.pyplot as plt
import numpy as np

HERE = os.path.dirname(os.path.abspath(__file__))
CSV_PATH = os.path.join(HERE, 'data.csv')
SRC_IMG = '/Users/csweet1/Documents/projects/CRS_research/figure-recovery-suite/corpora/owid-r6-1/charts/share-electricity-low-carbon/image.png'
OUT_PNG = os.path.join(HERE, 'replot.png')

# Series order and colors transcribed from chart_metadata.json (Phase 1).
# Order follows visual top-to-bottom legend layout in the source image.
SERIES_ORDER = ['Sweden', 'France', 'United Kingdom', 'Germany',
                'United States', 'World', 'China', 'India']
COLORS = {
    'Sweden':         '#9a4caf',
    'France':         '#c45a2d',
    'United Kingdom': '#88313a',
    'Germany':        '#9a6e3d',
    'United States':  '#1f3e6b',
    'World':          '#a2569f',
    'China':          '#577a99',
    'India':          '#2f8267',
}

# Load extracted data
series = defaultdict(list)
with open(CSV_PATH) as f:
    r = csv.DictReader(f)
    for row in r:
        series[row['series']].append((int(row['x']), float(row['y'])))

# Render figure at 850x600 (same as source). dpi=100 -> 8.5 x 6.0 in.
fig = plt.figure(figsize=(8.5, 6.0), dpi=100)
ax = fig.add_axes([0.06, 0.10, 0.80, 0.72])  # leaves OWID-like margins for title

for name in SERIES_ORDER:
    pts = sorted(series[name])
    xs = [p[0] for p in pts]
    ys = [p[1] for p in pts]
    ax.plot(xs, ys, color=COLORS[name], linewidth=1.4,
            marker='o', markersize=3, markeredgecolor=COLORS[name],
            markerfacecolor=COLORS[name], label=name)
    # End-of-line label (OWID style)
    if pts:
        ax.text(pts[-1][0] + 0.8, pts[-1][1], name, color=COLORS[name],
                fontsize=9, va='center', ha='left')

ax.set_xlim(1985, 2027)
ax.set_ylim(0, 100)
ax.set_xticks([1985, 1990, 1995, 2000, 2005, 2010, 2015, 2020, 2025])
ax.set_yticks([0, 20, 40, 60, 80, 100])
ax.set_yticklabels([f'{int(y)}%' for y in [0, 20, 40, 60, 80, 100]])
ax.tick_params(axis='both', length=0, labelsize=9)
for s in ['top', 'right', 'left']:
    ax.spines[s].set_visible(False)
ax.spines['bottom'].set_color('#888888')
ax.grid(axis='y', linestyle='--', color='#cccccc', linewidth=0.6)
ax.set_axisbelow(True)

fig.suptitle('Share of electricity generated by low-carbon sources',
             x=0.06, y=0.96, ha='left', fontsize=14, fontweight='bold')
fig.text(0.06, 0.87,
         'Measured as a percentage of total electricity produced in the country or region.\n'
         'Low-carbon sources include renewables and nuclear power.',
         fontsize=8, color='#444444')

plt.savefig(OUT_PNG, dpi=100)
print(f'Wrote {OUT_PNG}')

# Side-by-side composite for inspection
fig2, axes = plt.subplots(2, 1, figsize=(8.5, 12.0), dpi=100)
axes[0].imshow(mpimg.imread(SRC_IMG))
axes[0].set_title('source')
axes[0].axis('off')
axes[1].imshow(mpimg.imread(OUT_PNG))
axes[1].set_title('replot')
axes[1].axis('off')
side_path = os.path.join(HERE, 'side_by_side.png')
plt.savefig(side_path, dpi=80)
print(f'Wrote {side_path}')
